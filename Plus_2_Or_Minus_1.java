import java.util.*;
import java.lang.*;
import java.io.*;

class Plus_2_Or_Minus_1
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner scan = new Scanner (System.in);
		int t = scan.nextInt();
		while(t-->0){
		    int y = scan.nextInt();
		    if(y==0)
		    System.out.println(1);
		    else
		    System.out.println(y*3);
		    
		}

	}
}
